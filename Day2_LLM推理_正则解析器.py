"""
正则表达式常用符号速查：

基础符号：
- .   匹配任意单个字符
- \d  匹配数字（等价于[0-9]）
- \w  匹配字母、数字、下划线（等价于[a-zA-Z0-9_]）
- \s  匹配空白字符（空格、tab、换行）
- ^   匹配字符串开头
- $   匹配字符串结尾

数量符号：
- *   匹配0次或多次
- +   匹配1次或多次
- ?   匹配0次或1次
- {n} 匹配恰好n次
- {n,m} 匹配n到m次

特殊符号：
- |   或（或运算）
- []  字符类（匹配括号内的任意字符）
- ()  捕获组（提取括号内的内容）

贪婪vs非贪婪：
- .*  贪婪匹配（尽可能多匹配）
- .*? 非贪婪匹配（尽可能少匹配）

示例：
- r"(\w+)='([^']+)'"  匹配 foo='bar'
- r"(\d+)-(\d+)-(\d+)"  匹配 2024-01-15
- r"\[([^\]]+)\]"       匹配 [内容]

"""
