# ================================================================================

def llm_infer(query: str) -> str:
    """
    调用LLM模型进行推理
    
    参数:
        query: 用户的问题
    
    返回:
        模型生成的文本回复
    
    这个函数封装了调用LLM的完整流程：
    1. 构造消息列表（系统提示 + 用户问题）
    2. 调用API
    3. 提取返回内容
    """
    
    # client.chat.completions.create: 调用聊天完成API
    # 这是OpenAI/兼容API的标准调用方式
    
    resp = client.chat.completions.create(
        model=MODEL,  # 使用哪个模型
        
        # messages: 对话历史列表
        # 每个消息是一个字典，包含role和content
        messages=[
            # system: 系统角色，设置AI的行为规范
            {"role": "system", "content": SYSTEM_PROMPT},
            
            # user: 用户角色，放入用户的问题
            {"role": "user", "content": query}
        ],
        
        # temperature: 温度参数，控制输出的随机性
        # 0: 最确定性的输出，格式最稳定（推荐用于Agent）
        # 1.0: 正常随机性
        # 2.0: 高随机性，可能胡说八道
        temperature=0
    )
    
    # resp.choices[0]: 返回的第一个选择（因为我们只请求一个回复）
    # .message.content: 消息的实际内容
    # .strip(): 去除首尾空白字符（空格、换行等）
    return resp.choices[0].message.content.strip()

"""
llm_infer函数的工作流程：

1. 构造消息：
   [
     {"role": "system", "content": SYSTEM_PROMPT},  # 告诉AI规则
     {"role": "user", "content": query}            # 用户的问题
   ]

2. 发送API请求到Ollama

3. Ollama内部：
   - 加载模型qwen:7b
   - 读取system prompt，设置AI行为
   - 读取user content，理解问题
   - 生成符合格式的回复

4. 解析返回结果，提取content字段

5. 返回给调用者
"""

# ================================================================================
# 第7部分：【核心】手撕正则解析器
# ================================================================================

def parse_agent_output(output: str):
    """
    解析Agent输出，提取Thought和Action
    
    参数:
        output: LLM返回的原始文本
    
    返回:
        一个元组(type, tool, args, thought)
        - type: "finish"表示结束，"tool"表示要调用工具
        - tool: 工具名（如果是tool类型）
        - args: 参数字典（如果是tool类型）
        - thought: 思考过程
        - result: 结果（如果是finish类型）
    
    这个函数是最核心的部分，需要仔细理解：
    
    正则表达式基础：
    - r"pattern": raw string，避免转义字符问题
    - .: 匹配任意单个字符
    - *: 匹配前面的字符0次或多次
    - ?: 非贪婪匹配（尽可能少匹配）
    - (): 捕获组，提取括号内的内容
    - \s*: 匹配0个或多个空白字符
    - (?=...): 前瞻断言，匹配"跟着..."前面的部分
    """
    
    # ========================================================================
    # 第1步：定义正则表达式模式
    # ========================================================================
    
    # Thought匹配模式：
    # r"Thought:\s*(.*?)(?=\nAction:)"
    # 分解：
    # - "Thought:" 字面匹配"Thought:"
    # - \s* 匹配0个或多个空白字符（空格、tab等）
    # - (.*?) 捕获组，非贪婪匹配任意字符（这就是Thought的内容）
    # - (?=\nAction:) 前瞻断言，匹配到"\nAction:"之前为止
    #   （但不包含"\nAction:"，只取前面的内容）
    
    thought_pattern = r"Thought:\s*(.*?)(?=\nAction:)"
    
    # Action匹配模式：
    # r"Action:\s*(.*)"
    # 分解：
    # - "Action:" 字面匹配
    # - \s* 匹配空白
    # - (.*) 捕获剩余所有内容（贪婪匹配）
    
    action_pattern = r"Action:\s*(.*)"
    
    # ========================================================================
    # 第2步：使用正则提取数据
    # ========================================================================
    
    # re.search(): 在字符串中搜索匹配的模式
    # 返回第一个匹配的结果（Match对象），没找到返回None
    
    # .search(待搜索文本, re.S): re.S让.能匹配换行符
    thought_match = re.search(thought_pattern, output, re.S)
    
    # .group(1): 获取第1个捕获组的内容（就是.*?里面的内容）
    # .strip(): 去除首尾空白
    thought = thought_match.group(1).strip()
    
    # 同样的方法提取Action
    action_match = re.search(action_pattern, output)
    action = action_match.group(1).strip()
    
    # ========================================================================
    # 第3步：判断任务是否结束
    # ========================================================================
    
    # startswith(): 检查字符串是否以指定前缀开头
    if action.startswith("Finish"):
        # 如果是Finish格式：Finish[答案内容]
        # 提取答案：
        #   "Finish[已完成]" → "已完成"
        
        # replace(要替换的, 替换成什么): 字符串替换
        result = action.replace("Finish[", "").replace("]", "")
        
        # 返回类型为"finish"，表示任务完成
        # None表示没有工具名和参数
        # result是最终答案
        # thought是思考过程
        return "finish", None, result, thought
    
    # ========================================================================
    # 第4步：解析工具调用
    # ========================================================================
    
    # 到这里说明是工具调用，格式必须是：
    # 工具名(参数名="参数值")
    # 例如：get_weather(city="北京")
    
    # 提取工具名：
    # r"^(\w+)\(" 
    # - ^ 匹配字符串开头
    # - (\w+) 捕获组，\w匹配字母、数字、下划线，+表示1个或多个
    # - \( 字面匹配左括号
    # .group(1): 提取捕获的内容
    
    tool_name = re.match(r"^(\w+)\(", action).group(1)
    
    # 提取参数：
    # r'(\w+)="([^"]+)"'
    # - (\w+) 捕获参数名
    # - = 字面匹配等号
    # - "([^"]+)" 捕获双引号内的内容
    # re.findall(): 找出所有匹配，返回列表
    # dict(...): 转换成字典
    
    # 举例：如果action = 'get_weather(city="北京")'
    # re.findall会返回：[('city', '北京')]
    # dict()转换成：{'city': '北京'}
    args = dict(re.findall(r'(\w+)="([^"]+)"', action))
    
    # ========================================================================
    # 第5步：返回解析结果
    # ========================================================================
    
    # 返回一个元组，包含所有提取的信息
    # type="tool"表示需要调用工具
    # tool_name是要调用的工具名
    # args是参数字典
    # thought是思考过程
    return "tool", tool_name, args, thought


"""
================================================================================
parse_agent_output函数执行示例：

假设LLM返回的output是：
```
Thought: 用户想知道北京的天气，需要调用天气查询工具
Action: get_weather(city="北京")
```

执行过程：
1. thought_pattern匹配：
   "Thought:" + 空白 + "(.*?)" + "\nAction:"
   结果：thought = "用户想知道北京的天气，需要调用天气查询工具"

2. action_pattern匹配：
   "Action:" + 空白 + "(.*)"
   结果：action = 'get_weather(city="北京")'

3. 判断：action.startswith("Finish") → False（不是Finish）

4. 提取工具名：
   r"^(\w+)\(" 匹配 action
   结果：tool_name = "get_weather"

5. 提取参数：
   r'(\w+)="([^"]+)"' 在 action 中查找
   结果：[('city', '北京')]
   转换：args = {'city': '北京'}

6. 返回：
   ("tool", "get_weather", {'city': '北京'}, "用户想知道北京的天气...")

================================================================================
"""

